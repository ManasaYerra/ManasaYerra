class UpLoAl
{
	public static void main(String[]args)
	{
		System.out.println("The upper case letters : ");
		for(char i='A';i<='Z';i++)
		{
			System.out.println(i);
		}
			System.out.println("The lower case letters : ");
			for(char i='a';i<='z';i++)
		{
				System.out.println(i);
		}
	}
}