class Pattern8
{
	public static void main(String[] args) 
	{
		int n=1;
		for(int i=1;i<=3;i++)
		{
			for(int j=1;j<=3;j++)
			{
				int count=0;
				if(n>1)
				{
					for(int k=1;k<=n;k++)
					{
						if(n%1==0)
						{
							count++;
						}
					}
					if(count==2);
				}
		System.out.print(n++ + " ");
			}
			System.out.println(" ");
		}
	}
}
